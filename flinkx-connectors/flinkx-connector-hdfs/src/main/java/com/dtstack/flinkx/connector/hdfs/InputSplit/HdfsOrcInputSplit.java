package com.dtstack.flinkx.connector.hdfs.InputSplit;

import org.apache.flink.core.io.InputSplit;

import org.apache.hadoop.hive.ql.io.orc.OrcSplit;

import java.io.ByteArrayInputStream;
import java.io.ByteArrayOutputStream;
import java.io.DataInputStream;
import java.io.DataOutputStream;
import java.io.IOException;
import java.util.ArrayList;

/**
 * Date: 2021/06/08 Company: www.dtstack.com
 *
 * @author tudou
 */
public class HdfsOrcInputSplit implements InputSplit {
    private static final long serialVersionUID = 1L;
    int splitNumber;
    byte[] orcSplitData;

    public HdfsOrcInputSplit(OrcSplit orcSplit, int splitNumber) throws IOException {
        this.splitNumber = splitNumber;
        ByteArrayOutputStream baos = new ByteArrayOutputStream();
        DataOutputStream dos = new DataOutputStream(baos);
        orcSplit.write(dos);
        orcSplitData = baos.toByteArray();
        baos.close();
        dos.close();
    }

    public OrcSplit getOrcSplit() throws IOException {
        ByteArrayInputStream bais = new ByteArrayInputStream(orcSplitData);
        DataInputStream dis = new DataInputStream(bais);
        OrcSplit orcSplit = new OrcSplit(null, 0, 0, null, null, false, false, new ArrayList<>());
        orcSplit.readFields(dis);
        bais.close();
        dis.close();
        return orcSplit;
    }

    @Override
    public int getSplitNumber() {
        return splitNumber;
    }
}
